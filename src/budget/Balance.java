package budget;

public class Balance
{
	private Integer id;
	private Integer transactId;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getTransactId() {
		return transactId;
	}
	public void setTransactId(Integer transactId) {
		this.transactId = transactId;
	}
}
